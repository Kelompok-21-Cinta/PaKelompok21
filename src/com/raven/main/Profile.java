    private void txttambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txttambahActionPerformed

    // Mendapatkan nilai dari JTextField
    String userName = jTextField2.getText(); // assuming jTextField2 is for user name
    String email = jTextField3.getText(); // assuming jTextField3 is for email
    String password = jTextField4.getText(); // assuming jTextField4 is for password
    String status = jTextField5.getText(); // assuming jTextField5 is for status
    String alamat = jTextField6.getText(); // assuming jTextField6 is for alamat
    String undangan = jTextField7.getText(); // assuming jTextField7 is for undangan

    try {
        String sql = "INSERT INTO user (UserName, Email, Password, Status, Alamat, Undangan) VALUES (?, ?, ?, ?, ?, ?)";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, userName);
        stmt.setString(2, email);
        stmt.setString(3, password);
        stmt.setString(4, status);
        stmt.setString(5, alamat);
        stmt.setString(6, undangan);

        stmt.executeUpdate();
        
        showDataInTable(); // Memperbarui tabel setelah operasi tambah
        resetTextFields(); 
    } catch (SQLException e) {
        e.printStackTrace();
    }


    
    }//GEN-LAST:event_txttambahActionPerformed

    private void txtubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtubahActionPerformed

    String userIDText = jTextField1.getText();

    if (!userIDText.isEmpty()) {
        try {
            int userID = Integer.parseInt(userIDText);
            String userName = jTextField2.getText(); // assuming jTextField2 is for user name
            String email = jTextField3.getText(); // assuming jTextField3 is for email
            String password = jTextField4.getText(); // assuming jTextField4 is for password
            String status = jTextField5.getText(); // assuming jTextField5 is for status
            String alamat = jTextField6.getText(); // assuming jTextField6 is for alamat
            String undangan = jTextField7.getText(); // assuming jTextField7 is for undangan

            String sql = "UPDATE user SET UserName=?, Email=?, Password=?, Status=?, Alamat=?, Undangan=? WHERE UserID=?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, userName);
            stmt.setString(2, email);
            stmt.setString(3, password);
            stmt.setString(4, status);
            stmt.setString(5, alamat);
            stmt.setString(6, undangan);
            stmt.setInt(7, userID);

            stmt.executeUpdate();
            showDataInTable(); // Memperbarui tabel setelah operasi ubah
        } catch (NumberFormatException | SQLException e) {
            e.printStackTrace();
        }
    } else {
        // Tindakan jika userIDText kosong
        // Misalnya, menampilkan pesan kesalahan kepada pengguna
    }
    resetTextFields(); 
    txttambah.setEnabled(true);




    }//GEN-LAST:event_txtubahActionPerformed

