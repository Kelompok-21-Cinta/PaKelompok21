        private void showDataInTable() {

        try {
            String sql = "SELECT UserID, UserName, Email, Password, Status, Alamat, Undangan FROM user";
            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();

            DefaultTableModel model = (DefaultTableModel) txtTabel.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                int userID = rs.getInt("UserID");
                String userName = rs.getString("UserName");
                String email = rs.getString("Email");
                String password = rs.getString("Password");
                String status = rs.getString("Status");
                String alamat = rs.getString("Alamat");
                String undangan = rs.getString("Undangan");

                model.addRow(new Object[]{userID, userName, email, password, status, alamat, undangan});
            }
            txttambah.setEnabled(true);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    

